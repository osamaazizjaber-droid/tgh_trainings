export const getGovernorates = () => Object.keys(IRAQ_LOCATIONS);

export const getDistricts = (governorate) => {
  if (!governorate || !IRAQ_LOCATIONS[governorate]) return [];
  return Object.keys(IRAQ_LOCATIONS[governorate].districts);
};

export const getSubdistricts = (governorate, district) => {
  if (!governorate || !district) return [];
  const gov = IRAQ_LOCATIONS[governorate];
  if (!gov) return [];
  return gov.districts[district] || [];
};
